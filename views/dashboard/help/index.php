(function() {
    // Smooth scroll + active sidebar link on click
    var navItems = document.querySelectorAll('.hg-nav-item');
    navItems.forEach(function(item) {
        item.addEventListener('click', function(e) {
            var href = this.getAttribute('href');
            if (!href || href.charAt(0) !== '#') return;
            var target = document.querySelector(href);
            if (!target) return;
            e.preventDefault();
            target.scrollIntoView({ behavior: 'smooth', block: 'start' });
            navItems.forEach(function(n) { n.classList.remove('active'); });
            this.classList.add('active');
        });
    });

    // Update active link on scroll
    var sections = document.querySelectorAll('.hg-section');
    function updateActive() {
        var scrollPos = window.scrollY + 120;
        var current = null;
        sections.forEach(function(s) {
            if (s.offsetTop <= scrollPos) current = s.id;
        });
        if (current) {
            navItems.forEach(function(n) {
                n.classList.toggle('active', n.getAttribute('href') === '#' + current);
            });
        }
    }
    window.addEventListener('scroll', updateActive, { passive: true });
    updateActive();

    // Client-side search
    var search = document.getElementById('hgSearch');
    var noResults = document.getElementById('hgNoResults');
    if (search) {
        search.addEventListener('input', function() {
            var q = this.value.trim().toLowerCase();
            var visibleCount = 0;
            sections.forEach(function(s) {
                var haystack = (s.dataset.hgSection || '') + ' ' + s.textContent.toLowerCase();
                var match = q === '' || haystack.indexOf(q) !== -1;
                s.style.display = match ? '' : 'none';
                if (match) visibleCount++;
            });
            // Dim sidebar items whose section is hidden
            navItems.forEach(function(n) {
                var href = n.getAttribute('href');
                if (!href || href.charAt(0) !== '#') return;
                var target = document.querySelector(href);
                if (target) n.style.opacity = (target.style.display === 'none') ? '.35' : '';
            });
            if (noResults) noResults.style.display = (visibleCount === 0) ? 'block' : 'none';
        });
    }
})();
</script>
